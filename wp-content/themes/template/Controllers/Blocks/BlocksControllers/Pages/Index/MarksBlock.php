<?php
namespace Controllers\Blocks\BlocksControllers\Pages\Index;

class MarksBlock extends \Controllers\Blocks\BlockAbstractController {


    static $options =
        [
            'template'  => './blocks/Pages/Index/marks',
            'id' => null,
            'fields' => ['title','items'],

        ];

    public function __construct($input,$id=null)
    {
        parent::__construct(static::$options['template'], !$id?static::$options['id']:$id , $input);



        $this->set_data();
    }

    protected function set_data()
    {
        parent::set_data(); // TODO: Change the autogenerated stub
        $taxs = \Controllers\PostsAndTax\TaxProductCategory::get_allTerms();


            foreach ($taxs as $key=>$item )
            {
                $taxs[$key]->link = get_page_link(76).'?mark='.$item->slug;
            }
        $this->data['items']= $taxs;

    }
}